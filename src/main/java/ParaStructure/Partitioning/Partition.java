package ParaStructure.Partitioning;

import java.io.ByteArrayOutputStream;
import java.io.ObjectOutputStream;
import java.util.ArrayList;
import java.util.List;

public class Partition {
    public List<Integer> partition=new ArrayList<Integer>();

    public static byte[] toByteArray(Partition partition){
        byte[] bytes=null;
        ByteArrayOutputStream btArrOutputStream=new ByteArrayOutputStream();
        try{
            ObjectOutputStream objOutputStream=new ObjectOutputStream(btArrOutputStream);
            objOutputStream.writeObject(partition);
            objOutputStream.flush();
            bytes = btArrOutputStream.toByteArray();
            btArrOutputStream.close();
            objOutputStream.close();
            return bytes;
        }catch (Exception e){
            e.printStackTrace();
            return null;
        }
    }
}
